package org.glandais.srtm.loader;

public class SRTMException extends Exception {

	private static final long serialVersionUID = 4625627414812635479L;

	public SRTMException() {
		super();
	}

	public SRTMException(String message, Throwable cause) {
		super(message, cause);
	}

	public SRTMException(String message) {
		super(message);
	}

	public SRTMException(Throwable cause) {
		super(cause);
	}

}
